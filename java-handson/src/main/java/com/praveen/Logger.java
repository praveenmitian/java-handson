package com.praveen;

/**
 * Getter, setter demo.
 * 
 * @author PRAVEEN
 *
 */
public class Logger {
	private String format;

	/**
	 * Getter to get value.
	 * 
	 * @return String
	 */
	public String getFormat() {
		return this.format;
	}

	/**
	 * Setter to set value.
	 * 
	 * @param format
	 */
	public void setFormat(String format) {
		this.format = format;
	}
}
